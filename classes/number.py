# number basics #
myInt = 100
myFloat = 3.1415

# operations #
print(myInt * 5)
# "**" is raised to the power of (2 ^ 2 = 4)
print(myInt ** 2)

# add a new line
print("\n")

# equations
print(myFloat)
# print the value of the equation
print(myFloat * (myInt / 1.23))

# str() turns any variable into a string
print("\nThe sum of all variables is: " + str(myInt + myFloat))
